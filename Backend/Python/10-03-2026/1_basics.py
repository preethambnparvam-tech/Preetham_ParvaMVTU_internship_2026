#Primitive Datatypes:
#int,string,float,complex,boolean

fname = "Preethem"
lname = "Gowda"
print(fname)
print(lname)
print(fname, lname)
print(type(fname))

age = 21
print(age, type(age))

height = 150.35
print(height, type(height))

comp = 5+3j
print(comp, type(comp))

isIndian = True
print(isIndian, type(isIndian))

#Non-Primitive Datatypes:
#List,tuple, set, dictionary

fruits = ["Apple", "Banana", "Chikoo", "Dragon Fruit"]
print(fruits, type(fruits))

cars = ("Toyota Innova", "Tata Punch", "Skoda Rapid", "Thar Roxx")
print(cars, type(cars))

evenNumbers = {2,4,6,8,10}
print(evenNumbers, type(evenNumbers))

profile = {"name": "Preetham", "age": 21, "company": "ParvaM", "Programming-language": ["C","C++", "Python", "Java"]}
print(profile, type(profile))